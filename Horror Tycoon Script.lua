		Toggle = not Toggle
		if Toggle then
	
			game.CoreGui.Main.Main.Local.Fly.Frame.BackgroundColor3 = Color3.new(0, 255, 0)
		    fly()
		else
	
				game.CoreGui.Main.Main.Local.Fly.Frame.BackgroundColor3 = Color3.new(255, 0, 0)
			endFlying()
		end
	end)
