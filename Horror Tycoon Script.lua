					bodyVel.Velocity = camera.CFrame.LookVector*speed*100
				end
			end
	
		end
	end)
	
	uis.InputEnded:Connect(function(Input)
		if Input.KeyCode == Enum.KeyCode.W and flying == true and wdown == true then
